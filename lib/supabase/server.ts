// Admin client that truly bypasses RLS.
// The SSR createServerClient forwards the user's session JWT as the Authorization Bearer,
// so PostgREST uses the "authenticated" role and RLS still applies.
// createAdminClient uses @supabase/supabase-js with no session, so it sends the service
// role key itself as the bearer → PostgREST grants the "service_role" role → RLS bypassed.
export function createAdminClient() {
  return createSupabaseClient(
    process.env.NEXT_PUBLIC_SUPABASE_URL!,
    process.env.SUPABASE_SERVICE_ROLE_KEY!,
    { auth: { autoRefreshToken: false, persistSession: false } }
  );
}
